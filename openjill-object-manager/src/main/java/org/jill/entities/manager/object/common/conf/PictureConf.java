package org.jill.entities.manager.object.common.conf;

public class PictureConf {
    /**
     * Tileset of picture.
     */
    private int tileset;

    /**
     * Tile of picture.
     */
    private int tile;

    /**
     * Tileset of picture.
     *
     * @return index of tileset
     */
    public int getTileset() {
        return tileset;
    }

    /**
     * Tile of picture.
     *
     * @return index of tile
     */
    public int getTile() {
        return tile;
    }
}
